import streamlit as st
from prediction import HousePricePredictor

# Page Configuration
st.set_page_config(
    page_title="House Price Predictor",
    page_icon="🏡",
    layout="centered"
)

# Application Header
st.title("🏡 House Price Prediction using ANN")
st.markdown("""
Welcome to the House Price Predictor Web App! 
This tool utilizes a trained Artificial Neural Network (ANN) to estimate real estate prices based on property features.
""")

# Load the predictor singleton
@st.cache_resource
def load_model():
    try:
        return HousePricePredictor(model_path='models/ann_model.keras', scaler_path='models/scaler.pkl')
    except Exception as e:
        return None

predictor = load_model()

if predictor is None:
    st.error("⚠️ **Model files not found!**")
    st.info("Please ensure you have run `python train.py` to generate the `.keras` model and `scaler.pkl` files before launching the app.")
else:
    # Sidebar for Input Features
    st.sidebar.header("Enter Property Details")
    
    area = st.sidebar.number_input("Area (sq ft)", min_value=500, max_value=15000, value=2000, step=100)
    bedrooms = st.sidebar.number_input("Bedrooms", min_value=1, max_value=10, value=3, step=1)
    bathrooms = st.sidebar.number_input("Bathrooms", min_value=1, max_value=8, value=2, step=1)
    age = st.sidebar.number_input("Property Age (Years)", min_value=0, max_value=150, value=10, step=1)
    location = st.sidebar.selectbox("Location", ["Rural", "Suburban", "Urban"])
    
    # One-hot encode the location based on training data dummies
    # Rural is dropped as baseline (Location_Suburban=0, Location_Urban=0)
    loc_suburban = 1 if location == "Suburban" else 0
    loc_urban = 1 if location == "Urban" else 0
    
    # Main content area
    st.subheader("Selected Features Overview")
    col1, col2, col3, col4, col5 = st.columns(5)
    col1.metric("Area", f"{area} sqft")
    col2.metric("Bedrooms", bedrooms)
    col3.metric("Bathrooms", bathrooms)
    col4.metric("Age", f"{age} years")
    col5.metric("Location", location)
    
    st.divider()
    
    # Prediction Trigger
    if st.button("Predict Market Price", type="primary", use_container_width=True):
        with st.spinner("Analyzing parameters through Neural Network..."):
            features = [area, bedrooms, bathrooms, age, loc_suburban, loc_urban]
            predicted_price = predictor.predict(features)
            
            st.success("Prediction Complete!")
            st.markdown(f"<h2 style='text-align: center; color: #4CAF50;'>Estimated Price: ${predicted_price:,.2f}</h2>", unsafe_allow_html=True)
            
        st.caption("Note: This prediction is generated by a 3-hidden-layer feedforward neural network trained on housing market data.")

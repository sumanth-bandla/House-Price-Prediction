import jsPDF from 'jspdf';

export interface ReportData {
  state: string;
  city: string;
  area: number;
  bedrooms: number;
  bathrooms: number;
  age: number;
  propertyType: string;
  furnishing: string;
  prediction: number;
  showROI: boolean;
}

export const downloadReport = (data: ReportData) => {
  const doc = new jsPDF();
  
  doc.setFontSize(22);
  doc.setTextColor(20, 184, 166); // Brand color (approx)
  doc.text("AuraEstates Valuation", 20, 20);
  
  doc.setFontSize(10);
  doc.setTextColor(100, 100, 100);
  doc.text(`Generated on ${new Date().toLocaleDateString()} at ${new Date().toLocaleTimeString()}`, 20, 28);
  
  doc.setDrawColor(220, 220, 220);
  doc.line(20, 35, 190, 35);

  doc.setFontSize(12);
  doc.setTextColor(50, 50, 50);
  
  const startY = 45;
  const lineSpacing = 10;
  
  doc.text(`Location: ${data.city}, ${data.state}`, 20, startY);
  doc.text(`Property Type: ${data.propertyType}`, 20, startY + lineSpacing);
  doc.text(`Configuration: ${data.bedrooms} BHK, ${data.bathrooms} Bathrooms`, 20, startY + lineSpacing * 2);
  doc.text(`Super Built-up Area: ${data.area} sq.ft.`, 20, startY + lineSpacing * 3);
  doc.text(`Property Age: ${data.age} Years`, 20, startY + lineSpacing * 4);
  doc.text(`Furnishing: ${data.furnishing}`, 20, startY + lineSpacing * 5);

  doc.setDrawColor(220, 220, 220);
  doc.line(20, startY + lineSpacing * 6.5, 190, startY + lineSpacing * 6.5);

  doc.setFontSize(16);
  doc.setTextColor(10, 10, 10);
  doc.text(`Estimated Market Value: INR ${data.prediction.toLocaleString('en-IN')}`, 20, startY + lineSpacing * 8);

  if (data.showROI) {
    const rentalYield = Math.round(data.prediction * 0.035);
    const appreciation = Math.round(data.prediction * 0.058);
    
    doc.setFontSize(14);
    doc.setTextColor(20, 184, 166);
    doc.text("Investment Analysis", 20, startY + lineSpacing * 10);
    
    doc.setFontSize(12);
    doc.setTextColor(50, 50, 50);
    doc.text(`Expected Rental Yield (~3.5%): INR ${rentalYield.toLocaleString('en-IN')} / year`, 20, startY + lineSpacing * 11);
    doc.text(`Expected Annual Appreciation (~5.8%): INR ${appreciation.toLocaleString('en-IN')} / year`, 20, startY + lineSpacing * 12);
  }

  doc.setFontSize(9);
  doc.setTextColor(150, 150, 150);
  doc.text("Disclaimer: This valuation is generated by an artificial neural network model and should be", 20, 280);
  doc.text("used for informational purposes only. It does not constitute professional financial advice.", 20, 285);

  doc.save(`AuraEstates_Report_${data.city.replace(/\\s+/g, '_')}.pdf`);
};
